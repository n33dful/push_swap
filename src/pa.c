#include "../incl/push_swap.h"

void	pa(t_stack **a, t_stack **b)
{
	t_stack *tmp;

	if ((*b))
	{
		tmp = (*b);
		(*b) = (*b)->next;
		tmp->next = (*a);
		(*a) = tmp;
	}
}
