#include "libft.h"

char	*ft_strdup(char const *s)
{
	char	*ptr;
	size_t	len;
	int		i;

	i = 0;
	len = ft_strlen(s);
	if (!(ptr = ft_strnew(len)))
		return (NULL);
	while (s[i])
	{
		ptr[i] = s[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
